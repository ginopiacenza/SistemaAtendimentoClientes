public class Elemento {
    String id;
    String descricao;
    String dataHora;
    String nomeCliente;

    public Elemento(String id, String descricao, String dataHora, String nomeCliente) {
        this.id = id;
        this.descricao = descricao;
        this.dataHora = dataHora;
        this.nomeCliente = nomeCliente;
    }
}
